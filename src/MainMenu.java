    private void undoBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_undoBtActionPerformed
        Stack<String[]> updatedPost = core.Memory("undo"); 
        
        if(!updatedPost.get(0)[0].matches("\\d+")){
            JOptionPane.showMessageDialog(null, updatedPost.get(0)[0]);
        }else{
            DefaultTableModel table = (DefaultTableModel)listTable.getModel();
            table.setRowCount(0);

            for(String[] element : updatedPost){
                System.out.println(Arrays.toString(element));
            }

            postToTable(updatedPost);
        }
    }//GEN-LAST:event_undoBtActionPerformed

