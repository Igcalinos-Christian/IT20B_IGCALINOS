    private void getIncomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getIncomeActionPerformed
        String input, income;
        input = inputArea.getText();
        income = incomeDisplay.getText();
        
        //Prevents non-numerics from being sent.
        if(!input.matches("\\d+")){
            JOptionPane.showMessageDialog(null, "NUMERICS ONLY!");
        }else{
            
            //Sends numerics to "addIncome" method to be processed.
            if(income.equals("")){
                String newIncome = core.addIncome(input, "0");
                incomeDisplay.setText(newIncome);
            }else{
                String newIncome = core.addIncome(input, income);
                incomeDisplay.setText(newIncome);
            }
        }
        
        inputArea.setText("");
    }//GEN-LAST:event_getIncomeActionPerformed

    private void getExpenseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getExpenseActionPerformed
        String input, income, item;
        input = inputArea.getText();
        income = incomeDisplay.getText();
        item = itemInput.getText();
        
        //Prevents empty fields and incorrect inputs from being sent.
        if(input.equals("") || income.equals("") || item.equals("")){
            JOptionPane.showMessageDialog(null, "Please Check the fields");
        }else if(!input.matches("\\d+")){
            JOptionPane.showMessageDialog(null, "NUMERICS ONLY!");
        }else{
            
            // To get difference of input and income, then post it.
            String incomeLeft = core.getExpenses(input, income);
            
            if(!incomeLeft.matches("\\d+")){
                JOptionPane.showMessageDialog(null, incomeLeft);
            }else{
            
            incomeDisplay.setText(incomeLeft);
            inputArea.setText(""); // these two clears respective fields
            itemInput.setText("");
            
            // To display input(price) and item/task on the Table.
            String [] tableData = {input, item};
            DefaultTableModel table = (DefaultTableModel)listTable.getModel();
            table.addRow(tableData);
            
            // Stores data in a linked list.
            core.addData(incomeLeft, income, input, item);
            
            }
        }
    }//GEN-LAST:event_getExpenseActionPerformed

