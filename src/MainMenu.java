    public void postToTable(Stack<String[]> toPost){
        //Initalizes a table model for the jTable.
        DefaultTableModel table = (DefaultTableModel) listTable.getModel();
        
        if(toPost.size() == 1){
            table.setRowCount(0);
        }else{
            table.setRowCount(0); //prevents duplicates.
        
            for(String[] element: toPost){
                System.out.println(element);
                table.addRow(new String[] {element[2], element[3]}); //adds a row and its contents to the table.
            }
        }
    }
    
